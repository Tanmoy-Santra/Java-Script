// Arithmetic operations
let num1 = 10;
let num2 = 5;

console.log("Arithmetic Operations:");
console.log("Addition:", num1 + num2);
console.log("Subtraction:", num1 - num2);
console.log("Multiplication:", num1 * num2);
console.log("Division:", num1 / num2);
console.log("Modulus:", num1 % num2);

// String operations
let str1 = "Hello";
let str2 = "World";

console.log("\nString Operations:");
console.log("Concatenation:", str1 + " " + str2);
console.log("Length of str1:", str1.length);
console.log("Uppercase:", str1.toUpperCase());
console.log("Lowercase:", str2.toLowerCase());

// Comparison operations
let a = 10;
let b = 5;

console.log("\nComparison Operations:");
console.log("a > b:", a > b);
console.log("a < b:", a < b);
console.log("a >= b:", a >= b);
console.log("a <= b:", a <= b);
console.log("a === b:", a === b);
console.log("a !== b:", a !== b);

// Logical operations
let x = true;
let y = false;

console.log("\nLogical Operations:");
console.log("x && y:", x && y);
console.log("x || y:", x || y);
console.log("!x:", !x);
console.log("!y:", !y);

// Assignment operations
let c = 10;

console.log("\nAssignment Operations:");
c += 5;
console.log("c += 5:", c);
c -= 3;
console.log("c -= 3:", c);
c *= 2;
console.log("c *= 2:", c);
c /= 4;
console.log("c /= 4:", c);
c %= 2;
console.log("c %= 2:", c);
